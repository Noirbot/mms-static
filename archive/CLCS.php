            <h6>Contemporary Literature &amp; Composition, with Speech/Forensics 1 (CL) </h6>
            <div> <img src="pictures/PPT FF.JPG" alt="" width="300" height="225" class="project-img" /> 
			<p>Grades: 9-12</p> <br />
			<p>Speech/Comp with an added literature component to meet Georgia Accrediting Commission standards. </p> <br />
			<p>Total credits: 1.5 </p> <br />
			<p>DOE course names/codes: </p>
			<p>Contemporary Literature and Composition (23.03400 core or elective, depending on time spent writing) 
			and Speech/Forensics 1 (23.04600, elective)&lowast; . </p>
			<p> Choose one 90-minute speech/comp (SPCO) class per week (see <a href="locations.php#locations">Locations</a>)			
			PLUS plan to attend three, two-hour Friday seminars per semester as well as seminar prep and follow through at home. See unit plan for specific dates. </p>
			<p>Location of seminars: Dekalb County's Northlake Library, 10:00 to noon, and Atlanta/Fulton County's Northeast/Spruill Oaks Library in John's Creek, 1:00 to 3:00. PLease choose one of these locations when you register.</p>
			<p><b>Course Content:</b>  
			Students will cover the all materials of a SPCO class, attending once a week. 
			See that course description for writing and speech components of the class.</p>
			<br />
			<p>In addition, CL students work in six units of literature including modern short stories, poetry, drama, and nonfiction, 
			including works from Perrine's <em>Literature: Structure, Sound, and Sense</em> (ISBN 0155511084), teacher handouts, and Internet resources. 
			Students are taught the rhetorical framework of fiction and nonfiction, investigating literary analysis skills one can build on for future AP classwork if desired. 
			Optional summer reading can also contribute to literature requirements if parents add that in. 
			Students have a chance to be creative with their own writing and illustration, character role playing, and oral interpretation. Classes and discussions are upbeat and activities are varied. See unit plan for specific dates.
			</p> <br />
			<p>Text not included in price of class. It is usually about $10, used, and can be purchased online or from a former Shuman AP Lit or CLS student. 
			Please consider a book that does not have significant markings to distract the student?s reading, and to allow the student to make her own marks in the book.
			</p>

			<p>&lowast; Final grade reports list this class offering as two separate classes for 1.5 credits, and parents can work with instructor to customize course titles 
			that fit the student's transcript (with alternatives such as Ninth, Tenth, Eleventh, or Twelfth Grade Lit/Comp, 
			plus a semester of Speech or Rhetoric or such).</p> 			

