			<p>Grades: 9-12</p> <br />
			<p>Speech/Composition (SPCO) </p> <br />
			<p>One credit </p> <br />
			<p>DOE course names/codes:</p>
			<p>Speech/Forensics 1 (23.04600, elective) and Advanced Composition (23.03400 core or elective, depending on time spent writing)</p>
			<p>One 90-minute class per week: see <a href="locations.php#locations">Locations</a></p>
			
			<p>*Final grade reports can list this class as two separate semester classes, with titles such as "Composition" or "Writing," 
			and "Speech Communication" or "Public Speaking." Class can be paired with a literature course to create a comprehensive high school 
			class in "lit and comp." (This is already arranged if you take Contemporary Lit/Comp, for 1.5 credits.) Class can also stand alone as a high school English class, depending on what a student needs in a year for language arts. Instructor can work with parents on 
			class name and credits to fit transcripts. Optional summer reading can contribute to literature requirements as well.</p>

			<p>This is an active class that takes place in an encouraging environment. The skills of speech and writing may be taught simultaneously since much of the structure and style of both are complementary. 
			The oral communication aspect of SPCO starts with non-intimidating assignments in order for students to build basic 
			communication skills with a group and comfort for themselves. The class progresses through stages until basic solo speeches found in 
			Speech 101 in most universities are presented, and life skills such as persuasive speaking and communicating in group settings are practiced. 
			We also explore job interview skills, non-verbal communication, proposal presentation, small group consensus, and impromptu speaking.  Students practice design and use of various visual aids such as PowerPoint, posters, and demonstration tools.
			</p>
						
			<p>The composition aspect of the course covers organization and editing skills needed to conquer the SAT and ACT essays, college/scholarship applications, 
			business letters, discussion/exam-style essays, speaker analyses, article responses, resumes, and other practical writing. Practical grammar study is included in editing workshops, useful for those who have a mind for grammar and/or those with an ear for error correction.
			Teacher feedback in writing and speaking focuses on encouragement and "next steps" rather than punitive or negative assessment. Having said that, there is accountability for each student to grow and work toward his or her writing being able to match up with Standard American English. Students often turn in writing assignments during the week via email, making this a part-online/part-classroom experience. Students practice meeting deadlines and assignment details, with penalties (and rewards!) increasing over the semesters.
			Graded work is promptly sent home for parent perusal, and then is corrected by the student and recorded in a folder, which is graded for its 
			thoroughness before being sent home to parents at the end of each semester. Spring projects include an MLA-style research paper on a career chosen by the student and a formal, persuasive speech.</p>
			
	
