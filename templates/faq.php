      <h6>What are the benefits of Advanced Placement classes? </h6>
      <p>
      To have "AP" on one's transcript, your instructor must be approved by the College Board. Students often get 5-10 pts added to their GPA course value for an AP class. Students also have the opportunity to sit for AP exams given only in May. Though the test is not required for one to receive class credit, and one does not need to take an AP class to take an AP exam, an exam with a good score can reward students course credit in college. A talk with college representatives or a look at a college website can determine how valuable AP classes and tests may be for a student. Some universities offer credit for up to two English classes for a good score on the AP English exam.
      </p> <br />
